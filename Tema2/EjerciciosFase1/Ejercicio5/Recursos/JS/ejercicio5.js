let num1 = Number(prompt("Ingrese primer número:"));
let num2 = Number(prompt("Ingrese segundo número:"));
let num3 = Number(prompt("Ingrese tercer número:"));
let num4 = Number(prompt("Ingrese cuarto número:"));

let suma = num1 + num2;
let producto = num3 * num4;

document.write(`La suma de los dos primeros es ${suma} y el producto del tercero 
                y el cuarto es ${producto}`);